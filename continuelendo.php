      <h1 class="news_title">O aprendizado Holístico</h1>
       <p>A estratégia alternativa é focar na funcionalidade da informação que você está aprendendo. Isso envolve a ligação de conceitos e compreensão da informação para ter uma ideia do todo. Aqui vão algumas idéias para você começar.</p>

      <p><strong>  1-Metáfora -</strong> Através delas você pode rapidamente organizar as informações, comparando uma ideia complexa a uma muito mais simples. Quando você personifica uma relação entre as informações, com analogias para melhorar sua compreensão, seu entendimento pode ficar mais simples. Por exemplo: Compare neurônios com ondas em uma corda. Faça metáforas comparando a insuficiência venosa de membros inferiores com uma represa…
      <p><strong> 2-Use todos os seu sentidos -</strong> Ideias abstratas são difíceis de memorizar porque elas fogem dos nossos sentidos. Tente aproximá-las daquilo que é sinestésico linkando essas ideias a sentimentos e imagens que que ajudem coloca-las em um contexto.
      <p><strong> 3-Ensine o assunto - </strong> Ache alguém que não entenda o tópico e ensine para ele. Esse exercício o força a organizar todo seu conhecimento. Gastar 5 minutos explicando um conceito pode poupar uma hora de estudos combinados com o mesmo efeito.
      <p><strong> 4-Não deixe ilhas - </strong>Quando você estuda atravéz de um livro, todo pedaço de informação deve estar conectado a outro que você já tem o conhecimento (progressão do conhecimento). Pessoas que aprendem rapidamente fazem isso automaticamente, porém, se você deixar ilhas de informação, você não será apto para acessá-las durante uma prova. Exercite a conexão entre os assuntos.
      <p><strong> 5-Teste sua Mobilidade –</strong> Uma boa maneira de saber se você ligou suficientemente as informações é testar se você pode mover-se pelos conceitos. Abra um documento do word e comece a explicar o assunto que você está trabalhando. Se você não conseguir “pular” entre as sessões, referenciando uma ideia para explicar uma outra, você não estará apto para “pensar adequadamente” entre esses assuntos durante uma prova.
      <p><strong> 6-Ache padrões - </strong>Procure padrões nas informações. A informação ficará mais simples de ser organizada se você puder identificar padrões mais amplos que são similares entre os diferentes tópicos. Na bioquímica, a maioria das reações obedecem o padrão de oxidação e redução. Entendendo este conceito, você pode prever a maioria dos processos bioquímicos que ocorrem em nosso corpo. Da mesma forma, esse conceito é amplamente aplicado na farmacologia.
      <p><strong> 7-Crie uma boa fundação - </strong> Ler muito e ter um conhecimento geral de muitos assuntos dá a você maior flexibilidade em achar padrões e metaforizar novos objetos de estudo. Quanto mais você já conhece, mais fácil de aprender.
      <p><strong> 8-Não force -</strong>  Não é necessário estudar muito antes das provas. Forçar o aprendizado durante os poucos dias que precedem uma prova é incrivelmente ineficiente. Ao invés disso tente linkar as ideias que lhe ocorrem [sobre o assunto], assim a recapitulação é muito melhor que a primeira tentativa de aprendizado. Estudar sempre é o conselho.
      <p><strong> 9-Construa modelos -</strong> Modelos são conceitos simples que não são verdades por eles mesmos, mas são úteis para descrever ideias abstratas. Uma experiência ou imagem consolidada pode criar um modelo que você conseguirá referenciar quando estiver tentando entender um novo conceito.
     <p> <strong> 10-O aprendizado está em sua cabeça - </strong> Ter um caderno organizado ou livros perfeitamente sublinhados não  importam para o entendimento da informação. Seu único objetivo deve ser compreender o novo conhecimento para isso “grudar” na sua memória. Não tenha medo em se atrapalhar quando está escrevendo suas ideias em um papel e conectando-as em sua cabeça. O uso de cadernos é apenas um meio para o aprendizado, e não um fim para o resultado</p>
